def pick_balanced(items, count):
    """Выбираем по одному из как можно большего числа разных разделов."""
    by_section = {}
    for item in items:
        by_section.setdefault(item[0], []).append(item)
    sections = list(by_section.keys())
    random.shuffle(sections)
    result = []
    for sec in sections:
        if len(result) >= count:
            break
        candidates = by_section[sec]
        chosen = random.choice(candidates)
        result.append(chosen)
        by_section[sec].remove(chosen)
    # Добор
    if len(result) < count:
        remaining = [item for items in by_section.values() for item in items]
        random.shuffle(remaining)
        result.extend(remaining[:count - len(result)])
    return result


